    private void acceptRequestBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptRequestBtnActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();
        WorkRequest request = (WorkRequest) workRequestJTable.getValueAt(selectedRow, 0);

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please Select a row");
        } else {
            if(request instanceof IndustryWorkRequest){
            request.setReceiver(account);
            ((IndustryWorkRequest) request).setOrderResult("Request Sent");
            
            for (Network network : business.getNetworkList()) {
                for (Enterprise ent : network.getEnterpriseDirectory().getEnterpriseList()) {
                    if ((ent instanceof WareHouseEnterprise) && ((IndustryWorkRequest) request).getOrderResult().equals("Request Sent")) {

                        ent.getWorkQueue().getWorkRequestList().add(request);

                    }

                }
            }
            }
            populateTable();

        }

    }//GEN-LAST:event_acceptRequestBtnActionPerformed

