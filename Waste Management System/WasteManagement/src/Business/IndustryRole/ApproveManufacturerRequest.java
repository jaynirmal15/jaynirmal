    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();

        model.setRowCount(0);

        for (WorkRequest request : industryOrganisation.getWorkQueue().getWorkRequestList()) {
            if (request instanceof IndustryWorkRequest) {
                Object[] row = new Object[4];
                row[0] = ((IndustryWorkRequest) request);
                row[1] = request.getSender().getEmployee().getName();

                row[2] = ((IndustryWorkRequest) request).getIndquantity();
                row[3] = ((IndustryWorkRequest) request).getOrderResult();
                model.addRow(row);
            }

        }
    }

